package com.wbc.androidmvpdemo.core.http;

import com.wbc.androidmvpdemo.BuildConfig;
import com.wbc.androidmvpdemo.CommonUitl;
import com.wbc.androidmvpdemo.Constants;
import com.wbc.androidmvpdemo.core.http.api.HttpApi;

import java.io.File;
import java.io.IOException;
import java.util.concurrent.TimeUnit;

import okhttp3.Cache;
import okhttp3.CacheControl;
import okhttp3.Interceptor;
import okhttp3.OkHttpClient;
import okhttp3.Request;
import okhttp3.Response;
import okhttp3.logging.HttpLoggingInterceptor;
import retrofit2.Retrofit;
import retrofit2.adapter.rxjava2.RxJava2CallAdapterFactory;
import retrofit2.converter.gson.GsonConverterFactory;

